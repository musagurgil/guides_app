sayi = int(input("Sayı giriniz."))

if sayi>0:
    print("Pozitif")
elif sayi==0:
    print("Sıfır")
else:
    print("Negatif")

#README

### Sayı Pozitif, Negatif veya Sıfır mı?
# Bu Python programı, kullanıcının girdiği sayının pozitif, negatif veya sıfır olup olmadığını kontrol eden bir uygulamadır.

### Kullanım
# 1. Programı çalıştırın.
# 2. Bir sayı girin.
# 3. Program, girilen sayının durumunu kontrol eder ve ekrana sonucu yazdırır.

### Örnek
# Örnek giriş ve çıkış:
# Sayı giriniz: 5
# Pozitif
# Bu örnekte, kullanıcı tarafından girilen sayı pozitif olduğu için "Pozitif" mesajı ekrana yazdırılır.
#----------------------------------------------------------------
# Sayı giriniz: -3
# Negatif
# Bu örnekte, kullanıcı tarafından girilen sayı negatif olduğu için "Negatif" mesajı ekrana yazdırılır.
#----------------------------------------------------------------
# Sayı giriniz: 0
# Sıfır
# Bu örnekte, kullanıcı tarafından girilen sayı sıfır olduğu için "Sıfır" mesajı ekrana yazdırılır.

### Sınırlamalar
# - Program, kullanıcının sadece bir sayı girmesini bekler. Başka bir veri tipi girişi için hata mesajı verebilir.

### Kaynaklar
# Bu program, Python'ın basit bir koşul ifadesi kullanarak sayının pozitif, negatif veya sıfır olup olmadığını kontrol eder.

