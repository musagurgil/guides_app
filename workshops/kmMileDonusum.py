donusumOrani = 0.621371192
km = int(input("Kaç km?"))

mil = km * donusumOrani
print(str(km) +" Km = "+str(mil)+" mil eder")

#README

### Km-Mil Dönüşümü
# Bu Python programı, kullanıcının girdiği kilometre değerini mil birimine dönüştüren bir dönüşüm uygulamasıdır.

### Kullanım
# 1. Programı çalıştırın.
# 2. Bir kilometre değeri girin.
# 3. Program, girilen kilometre değerini mil birimine dönüştürür.
# 4. Dönüştürülen değer ekrana yazdırılır.

### Örnek
# Örnek giriş ve çıkış:
# Kaç km? 10
# 10 Km = 6.21371192 mil eder
# Bu örnekte, kullanıcı tarafından girilen kilometre değeri 10 olarak belirlenmiştir. Program, bu değeri mil birimine dönüştürerek sonucu "10 Km = 6.21371192 mil eder" şeklinde ekrana yazdırır.

### Sınırlamalar
# - Program, sadece sayısal değerleri kabul eder. Harf, özel karakterler veya boşluklar hatalara neden olabilir.

### Kaynaklar
# Bu program, birim dönüşüm hesaplamaları için sabit bir dönüşüm oranını kullanmaktadır.

