### Değişkenlerin Değerlerini Takas Etme
# Bu Python programı, iki değişkenin değerlerini birbirleriyle takas etmek için kullanılır. Programın çalışması sonucunda, değişkenlerin değerleri birbirleriyle değişir.

### Kullanım
# 1. Programı çalıştırın.
# 2. İlk değişkenin başlangıç değerini belirleyin.
# 3. İkinci değişkenin başlangıç değerini belirleyin.
# 4. Program, değişkenlerin değerlerini takas eder.
# 5. Değişkenlerin yeni değerlerini ekrana yazdırır.

### Örnek
# Örnek giriş ve çıkış:
# x = 10
# y = 20
# x, y = y, x
# print("x =", x)
# print("y =", y)
# Bu örnekte, başlangıçta `x` değişkeninin değeri 10 ve `y` değişkeninin değeri 20 olarak tanımlanmıştır. Programın çalışması sonucunda, `x` ve `y` değişkenlerinin değerleri birbirleriyle takas edilir. Sonuç olarak, ekrana şu çıktı yazdırılır:
# x = 20
# y = 10


### Sınırlamalar
# - Programın doğru çalışabilmesi için iki değişkenin başlangıç değerlerinin tanımlanmış olması gerekmektedir.

### Kaynaklar
